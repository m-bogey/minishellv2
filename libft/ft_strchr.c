#include "libft.h"

char	*ft_strchr(const char *s, int n)
{
	while ((*s))
	{
		if (*s == (char)n)
			return ((char *)s);
		s++;
	}
	if (*s == (char)n)
		return ((char *)s);
	return (NULL);
}
